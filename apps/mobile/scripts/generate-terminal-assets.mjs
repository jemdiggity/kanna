import fs from "node:fs";
import path from "node:path";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const appRoot = path.resolve(import.meta.dirname, "..");

function resolvePackageDir(packageName) {
  const packageJsonPath = require.resolve(`${packageName}/package.json`, {
    paths: [appRoot]
  });
  return path.dirname(packageJsonPath);
}

function readPackageFile(packageName, relativePath) {
  const packageDir = resolvePackageDir(packageName);
  return fs.readFileSync(path.join(packageDir, relativePath), "utf8");
}

const xtermCss = readPackageFile("@xterm/xterm", "css/xterm.css");
const xtermScript = readPackageFile("@xterm/xterm", "lib/xterm.js");
const fitAddonScript = readPackageFile("@xterm/addon-fit", "lib/addon-fit.js");

const outputPath = path.join(
  appRoot,
  "src",
  "screens",
  "xtermWebViewAssets.generated.ts"
);

const source = `// Generated by apps/mobile/scripts/generate-terminal-assets.mjs
// Do not edit by hand.

export const XTERM_WEBVIEW_CSS = ${JSON.stringify(xtermCss)};
export const XTERM_WEBVIEW_SCRIPT = ${JSON.stringify(xtermScript)};
export const XTERM_WEBVIEW_FIT_ADDON_SCRIPT = ${JSON.stringify(fitAddonScript)};
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, source);

console.log(`Wrote ${path.relative(appRoot, outputPath)}`);
